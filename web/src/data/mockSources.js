export const ingestionChartData = Array.from({ length: 48 }, (_, i) => {
  const t = new Date(now - (47 - i) * 30 * min)
  const base = 4800
  const variance = 800
  const eps = Math.max(0, base + (Math.random() - 0.5) * variance * 2)
  // Anomaly band: ±20% of base
  return {
    time: t.toISOString(),
    label: t.getHours().toString().padStart(2, '0') + ':' + (t.getMinutes() < 30 ? '00' : '30'),
    eps: Math.round(eps),
    upper: Math.round(base * 1.2),
    lower: Math.round(base * 0.8),
  }
})

